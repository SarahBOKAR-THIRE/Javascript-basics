const direction = "Haut";

switch (direction) {

    case "haut" :
        console.log("allez en haut !");
        break;
    case "bas" :
        console.log("allez en bas !");
        break;
    case "droite" :
        console.log("allez à droite !");
        break;
    case "gauche" :
        console.log("allez à gauche !");
        break;
    default :
        console.log("Rester sur place, pas bouger !");
            
}
