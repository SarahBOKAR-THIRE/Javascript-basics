const mois = 5;

switch(mois) {
    case 1 :
        console.log("janvier");
        break;

    case 2 : 
        console.log("février");
        break;

    case 3: 
        console.log("mars");
        break;

    case 4 :
        console.log("avril");
        break;

    case 5 : 
        console.log("mai");
        break;
    default :
        console.log("numéro de mois invalide")
}