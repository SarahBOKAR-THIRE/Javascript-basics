// Créez deux variables booléennes a et b avec des valeurs true ou false
let a = true;
let b = false;

// Utilisez les opérateurs logiques && (et) et || (ou) pour créer une expression conditionnelle complexe
let resultatEt = a && b; // L'expression est vraie si a ET b sont vrais
let resultatOu = a || b; // L'expression est vraie si a OU b est vrai

// Affichez le résultat de l'expression conditionnelle complexe dans la console
console.log("a && b :", resultatEt);
console.log("a || b :", resultatOu);
