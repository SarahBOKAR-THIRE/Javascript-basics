let n = 20;

// Initialisez une variable pour stocker la somme
let somme = 0;

// Utilisez une boucle for pour calculer la somme des nombres de 1 à n
for (let i = 1; i <= n; i++) {
    somme += i;
}

// Affichez le résultat dans la console
console.log("La somme des nombres de 1 à " + n + " est : " + somme);
