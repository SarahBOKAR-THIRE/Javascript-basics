// Création de deux variables numériques avec des valeurs différentes
let nombre1 =10;
let nombre2 = 5;

// Utilisation des opérateurs de comparaison
console.log("nombre1 < nombre2 :", nombre1 < nombre2); // Vérifie si nombre1 est inférieur à nombre2
console.log("nombre1 > nombre2 :", nombre1 > nombre2); // Vérifie si nombre1 est supérieur à nombre2
console.log("nombre1 <= nombre2 :", nombre1 <= nombre2); // Vérifie si nombre1 est inférieur ou égal à nombre2
console.log("nombre1 >= nombre2 :", nombre1 >= nombre2); // Vérifie si nombre1 est supérieur ou égal à nombre2
console.log("nombre1 == nombre2 :", nombre1 == nombre2); // Vérifie si nombre1 est égal à nombre2 (en valeur)
console.log("nombre1 != nombre2 :", nombre1 != nombre2); // Vérifie si nombre1 est différent de nombre2

// Résultats affichés dans la console
